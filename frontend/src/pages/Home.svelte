<h1>Verbessere die Welt mit Peer2Vehicle</h1>

<p id="homepage-text">
    Die derzeitige Situation in unseren Städten ist von überschüssigen
    parkierten Autos geprägt. Tatsächlich sind sie 96% der Zeit ungenutzt, und
    wenn sie verwendet werden, sind sie in den meisten Fällen nur von einer
    Person besetzt.
    Autos sind eine bedeutende Quelle für CO2-Emissionen. Eine effizientere
    Nutzung der vorhandenen Fahrzeuge durch gemeinsames Teilen könnte die Anzahl
    der Autos in unseren Städten signifikant reduzieren und damit einen
    wichtigen Beitrag zum Schutz unseres Planeten leisten.
</p>
